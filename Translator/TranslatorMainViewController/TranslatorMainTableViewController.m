-(instancetype)initWithAppController:(id<AppController>)appController{
    self = [super init];
    if(self){
        [self setAppController:appController];
    }
    return self;
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 2;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    if(section == 0){
        return 1;
    } else {
        return 0;
    }
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    if(indexPath.section == 0){
        if(indexPath.row ==0){
            UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:TRANSLATOR_HEADER_FIELD forIndexPath:indexPath];
            return cell;
        }
        return nil;
    } else {
        return nil;
    }
}


